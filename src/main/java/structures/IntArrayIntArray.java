package structures;

public class IntArrayIntArray {

    private int[] arrayA;
    private int[] arrayB;
    
    public IntArrayIntArray(int[] arrayA, int[] arrayB){
        this.arrayA = arrayA;
        this.arrayB = arrayB;
        
    }

    public int[] getArrayA() {
        return arrayA;
    }

    public int[] getArrayB() {
        return arrayB;
    }
}
